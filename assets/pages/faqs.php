	<p>
		<p class="bold">Why would I want to join?</p>
		We are a very active dive club that runs many trips every year, our club is full of very welcoming members and all are willing
		to help, plus we've got some great <a href="/Facilities">facilities</a> to offer.
	</p>

	<p>
		<p class="bold">What diving do you do?</p>
		That's really up to you, we can cater to all your diving needs. If you would like to cave dive we have members in the club 
		that can teach and join you, similarly if you want to dive wrecks, ice dive, reef dive, drift, the list really is endless.
	</p>

	<p>
		<p class="bold">Is it expensive?</p>
		It can be but it largely depends on what you want, some people want the latest technology and can pay thousands of pounds for 
		the privilege but for the most part you can find great deals on equipment, especially at the 
		<a href="http://www.diveshows.co.uk/" target="_blank">dive shows</a>. Please don't let the cost put you off though, for the first year
		you can rent almost all the kit from the club for a nominal fee, this gives you time to try out different equipment and buy it in
		bits and pieces.
	</p>

	<p>
		<p class="bold">I heard it's a dangerous sport, is that true?</p>
		<span class="bold">NO</span>, diving is not a dangerous sport, it has risks but with the training you receive and if you stay
		within the <a href="/assets/php/downloads.php?file=BSAC%202012%20Safe%20Diving.pdf" target="_blank">safe diving practices</a>, 
		diving can be one of the safest sports.
	</p>

	<p>
		<p class="bold">What facilities do you have?</p>
		We have a list of all the facilities SASAC offers, you can find that page <a href="/Facilities">here</a>.
	</p>

	<p>
		<p class="bold">When and where do you guys meet?</p>
		We meet at the clubhouse every Wednesday from 20:00, there are usually lectures on in the lecture theatre but the bar will
		be open and other members will be down to socialise. Club announcements are made at 20:30, these are just snippets of information on 
		upcoming events and dives. There is usually something happening on a Friday evening as well, during the summer, this will be a BBQ. 
		Of course we meet up for trips etc. and the annual dinner dance.
	</p>

	<p>
		<p class="bold">What do I want from a dive club?</p>
		Joining a dive club is not just about the diving. A dive club is just like a community, you join to socialise with like minded people
		and by joining a dive club you also get many other benefits such as use of the compressor, free lectures and all our other facilities.
	</p>

	<p>
		<p class="bold">I want to learn more?</p>
		Why not contact us or come down to the club and speak to someone, we will be more than happy to explain anything to you. You can
		contact us using the <a href="/Contact-Us">contact us</a> page.
	</p>

	<p>
		<p class="bold">How do I join?</p>
		That's simple. If you haven't already been down for a try dive, book one in first by using the <a href="/Contact-Us">contact us</a> 
		form or coming down the club and if you would like to join, you can sign up there and then.
